int	ft_isalnum(int i)
{
	int d;

	d = 1;
	if (!(i >= 'A' && i <= 'Z') &&
	!(i >= 'a' && i <= 'z') &&
	!(i >= '0' && i <= '9'))
	{
		d = 0;
		i++;
	}
	return (d);
}
